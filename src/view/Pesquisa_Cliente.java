    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        if(rbt_nome.isSelected() || rbt_cpf.isSelected()){
           if(rbt_nome.isSelected())
               readTabelaPesquisaNome(txt_pesquisa.getText());
           if(rbt_cpf.isSelected())
               readTabelaPesquisaCPF(txt_pesquisa.getText());
        }
        else
             readTabelaPesquisaNome(txt_pesquisa.getText());
           // JOptionPane.showMessageDialog(this, "Favor selecionar o tipo de pesquisa.");
    }//GEN-LAST:event_btn_pesquisarActionPerformed

    private void j_tb_clienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_j_tb_clienteMouseClicked
        if(j_tb_cliente.getSelectedRow() != -1){
            Cadastro_cliente cc = new Cadastro_cliente();
            cc.setVisible(true);
            cc.txt_nome.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 0).toString());
            cc.txt_cpf.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 1).toString());
            cc.txt_endereco.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 2).toString());
            cc.txt_numero.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 3).toString());
            cc.txt_comp.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 4).toString());
            cc.txt_bairro.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 5).toString());
            cc.txt_cidade.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 6).toString());
            cc.cmb_estado.setSelectedItem(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 7));
            cc.txt_fone.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 8).toString());
            cc.txt_celular.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 9).toString());
            cc.txt_email.setText(j_tb_cliente.getValueAt(j_tb_cliente.getSelectedRow(), 10).toString());
        }
    }//GEN-LAST:event_j_tb_clienteMouseClicked

