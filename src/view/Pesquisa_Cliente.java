    public void readTabelaPesquisaNome(String busca){
        DefaultTableModel tb = (DefaultTableModel) j_tb_cliente.getModel();
        tb.setNumRows(0);
        ClienteDAO cd = new ClienteDAO();
        
        for(Cliente c: cd.readPesquisaNome(busca)){
            tb.addRow(new Object[]{c.getNome(), c.getCpf(), c.getEndereco(), c.getNumero(),c.getComplemento(),
            c.getBairro(),c.getCidade(),c.getEstado(),c.getTelefone(),c.getCelular(),c.getEmail()});
        }
    }
    
    public void readTabelaPesquisaCPF(String busca){
        DefaultTableModel tb = (DefaultTableModel) j_tb_cliente.getModel();
        tb.setNumRows(0);
        ClienteDAO cd = new ClienteDAO();
        
        for(Cliente c: cd.readPesquisaCPF(busca)){
            tb.addRow(new Object[]{c.getNome(), c.getCpf(), c.getEndereco(), c.getNumero(),c.getComplemento(),
            c.getBairro(),c.getCidade(),c.getEstado(),c.getTelefone(),c.getCelular(),c.getEmail()});
        }
    }

