    public void readTabelaPesquisaCliente(String busca){
        DefaultTableModel tb = (DefaultTableModel) j_tb_exame.getModel();
        tb.setNumRows(0);
        ExameDAO ed = new ExameDAO();
        
        for(Exame e: ed.readPesquisaCliente(busca)){
            tb.addRow(new Object[]{e.getId(),e.getNome_cliente(),e.getCpf_cliente(),e.getNome_animal(),e.getEspecie(),e.getSuspeita(),
            e.isTratamento(),e.getTipo_ratamento(),e.getCategoria_exame(),e.getTipo_exame()});
        }
    }
